package Arrays;

import java.util.*;;

public class arrays2d {
   public static void main(String args[]) {
      // way to write 2D arrays
      // type [][] arrayName = new type [row] [column];
      // int [] [] number = new int [3][4];

      Scanner sc = new Scanner(System.in);
      int rows = sc.nextInt();
      int col = sc.nextInt();

      // initializating 2D array
      int number[][] = new int[rows][col];

      for (int i = 0; i < rows; i++) {
         for (int j = 0; j < col; j++) {
            number[i][j] = sc.nextInt();
         }
      }

      //output
      for (int i = 0; i < rows; i++) {
         for (int j = 0; j < col; j++) {
            System.out.print(number[i][j]+ " ");
         }
         System.out.println();
      }
      
   }
}
