import java.util.*;  //package


public class input_from_user {
    public static void main (String args[]){

        Scanner sc = new Scanner(System.in);  // this object 
        // for input we will use "System.in"

        // scanner will use "next" function to store input from the user
        String name = sc.nextLine(); // took input frm user 
        // "next" will use only one token

        String name1 = sc.nextLine();
        // to use entire line we will use nextLine
        System.out.println(name);


        //next()
        //nextLine()
    }
}
